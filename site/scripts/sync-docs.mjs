#!/usr/bin/env node
// sync-docs.mjs — walk the repo, copy every publishable markdown into
// site/src/content/docs/, normalize relative links, prepend Starlight
// frontmatter. Run as `npm run sync` before build.
//
// What's publishable:
//   - Every tracked `*.md` in the repo, EXCEPT:
//     - inside any `docs/` directory (gitignored — local dev notes)
//     - inside `node_modules/`, `target/`, `dist/`, `.git/`, `.claude/`,
//       `site/` (build artifacts + the site source itself)
//     - `AGENTS.md` and `CLAUDE.md` at any depth (local-only by name)
//
// What's NOT a manual list anywhere:
//   - The PAGES array used to be hand-curated. It isn't anymore.
//   - The Astro sidebar is generated by Starlight's `autogenerate`
//     against the synced tree, so adding a new README.md to the repo
//     is enough — nothing in site/ needs touching.

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "../../");
const DEST_ROOT = path.resolve(__dirname, "../src/content/docs/");

// Directories the walker never descends into. Mirrors .gitignore in
// spirit — we're not parsing .gitignore, just listing what matters.
const SKIP_DIRS = new Set([
  ".git",
  ".claude",
  "node_modules",
  "target",
  "dist",
  "docs",
  // site/ itself: walking it would race with our writes and import the
  // generated content back into the source.
  "site",
]);

// Files excluded by name at any depth.
const SKIP_FILES = new Set([
  "AGENTS.md",
  "CLAUDE.md",
]);

/** Decide whether to recurse into a directory. */
function shouldDescend(name) {
  return !SKIP_DIRS.has(name) && !name.startsWith(".");
}

/** Walk REPO_ROOT, yield every `.md` file path relative to REPO_ROOT. */
async function* walk(dir = REPO_ROOT, rel = "") {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  for (const e of entries) {
    if (e.isDirectory()) {
      if (!shouldDescend(e.name)) continue;
      yield* walk(path.join(dir, e.name), path.join(rel, e.name));
    } else if (e.isFile() && e.name.endsWith(".md") && !SKIP_FILES.has(e.name)) {
      yield path.join(rel, e.name);
    }
  }
}

/**
 * Compute (slug, title) for a source relative path.
 *
 * Examples:
 *   README.md                    → slug "index",                  title "hum"
 *   ensemble/README.md           → slug "ensemble/index",         title from H1
 *   thrum/WIRE.md                → slug "thrum/wire",             title from H1
 *   reports/compat-v0.10.3.md    → slug "reports/compat-v0.10.3", title from H1
 */
function deriveSlug(rel) {
  const dir = path.dirname(rel);
  const base = path.basename(rel, ".md");
  if (base === "README") {
    return dir === "." ? "index" : `${dir}/index`;
  }
  // Other markdown files keep their kebab-case basename.
  const lower = base.toLowerCase();
  return dir === "." ? lower : `${dir}/${lower}`;
}

/** Title: first `# Heading` line. Fallback: directory or basename. */
function deriveTitleAndDescription(body, rel) {
  let title = null;
  const h1 = body.match(/^#\s+(.+?)\s*$/m);
  if (h1) title = h1[1].trim();
  if (!title) {
    const dir = path.dirname(rel);
    title = dir === "." ? path.basename(rel, ".md") : dir.split("/").pop();
  }

  // Description: first non-heading, non-blockquote line of prose, or
  // the first blockquote (Starlight renders the description in TOC +
  // meta tags, and several existing READMEs use `> _tagline_` for it).
  let description = "";
  const lines = body.split("\n");
  let pastH1 = false;
  for (const line of lines) {
    const t = line.trim();
    if (!t) continue;
    if (/^#\s/.test(t) && !pastH1) {
      pastH1 = true;
      continue;
    }
    if (/^>\s+/.test(t)) {
      description = t.replace(/^>\s+/, "").replace(/^_(.+)_$/, "$1").trim();
      break;
    }
    if (pastH1 && !/^[#`>|*-]/.test(t)) {
      // Plain prose line — take it.
      description = t.slice(0, 200);
      break;
    }
  }
  return { title, description };
}

/**
 * Build a URL map for every page. Used to rewrite intra-repo links to
 * absolute site URLs (`/slug/`) regardless of where they appear.
 */
async function buildPageMap() {
  const map = new Map(); // repoRelativePath → "/site/url/"
  for await (const rel of walk()) {
    const slug = deriveSlug(rel);
    // index pages route to /<dir>/ on the site.
    const url = slug === "index" ? "/" : slug.replace(/\/index$/, "/").replace(/^/, "/").replace(/([^/])$/, "$1/");
    map.set(rel, url);
  }
  return map;
}

function escapeYaml(s) {
  return String(s).replace(/"/g, '\\"');
}

function frontmatter({ title, description }) {
  const lines = ["---", `title: "${escapeYaml(title)}"`];
  if (description) lines.push(`description: "${escapeYaml(description)}"`);
  lines.push("---", "");
  return lines.join("\n");
}

/**
 * Rewrite markdown links pointing at other files in this repo so they
 * resolve as absolute site URLs. External / anchor / non-repo links
 * pass through unchanged. Code fences are left alone.
 */
function rewriteLinks(body, srcRel, pageMap) {
  const srcDir = path.dirname(srcRel);
  let inFence = false;
  return body
    .split("\n")
    .map((line) => {
      if (/^\s*```/.test(line)) {
        inFence = !inFence;
        return line;
      }
      if (inFence) return line;
      return line.replace(/(\[[^\]]+\])\(([^)]+)\)/g, (m, label, target) => {
        if (/^[a-z][a-z0-9+.-]*:/i.test(target)) return m; // external URL scheme
        if (target.startsWith("#")) return m;             // anchor
        if (target.startsWith("/")) return m;             // already absolute

        // Strip any leading "./" then resolve against the source's dir.
        const cleaned = target.replace(/^\.\//, "");
        const resolved = path.posix.normalize(
          srcDir === "." ? cleaned : `${srcDir}/${cleaned}`,
        );

        // Direct hit (file is a page in the synced tree)?
        if (pageMap.has(resolved)) {
          return `${label}(${pageMap.get(resolved)})`;
        }
        // Trailing "/" — try the directory's README.
        if (resolved.endsWith("/") || !resolved.includes(".")) {
          const indexKey = path.posix.join(resolved, "README.md");
          if (pageMap.has(indexKey)) return `${label}(${pageMap.get(indexKey)})`;
        }
        // Trailing slash without an explicit file segment but resolved
        // landed on the directory itself.
        const indexAttempt = `${resolved.replace(/\/$/, "")}/README.md`;
        if (pageMap.has(indexAttempt)) return `${label}(${pageMap.get(indexAttempt)})`;

        // Source-tree pointer (Rust file, etc) — leave as-is; the
        // reader will resolve it on GitHub.
        return m;
      });
    })
    .join("\n");
}

async function syncOne(rel, pageMap) {
  const src = path.join(REPO_ROOT, rel);
  let body;
  try {
    body = await fs.readFile(src, "utf8");
  } catch (e) {
    console.warn(`[sync-docs] read failed ${rel}: ${e.code}`);
    return null;
  }
  const slug = deriveSlug(rel);
  const { title, description } = deriveTitleAndDescription(body, rel);

  // Strip the first H1 — Starlight renders title from frontmatter.
  body = body.replace(/^#\s+[^\n]+\n+/, "");
  body = rewriteLinks(body, rel, pageMap);

  const dest = path.join(DEST_ROOT, `${slug}.md`);
  await fs.mkdir(path.dirname(dest), { recursive: true });
  await fs.writeFile(dest, frontmatter({ title, description }) + body, "utf8");
  return slug;
}

